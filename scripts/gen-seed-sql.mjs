// Emits supabase/seed.sql from the canonical JSON in data/seed.
// Run:  node scripts/gen-seed-sql.mjs   (re-run whenever seed JSON changes)
import { readFileSync, writeFileSync, mkdirSync } from 'node:fs';
import { fileURLToPath } from 'node:url';
import { dirname, join } from 'node:path';

const __dirname = dirname(fileURLToPath(import.meta.url));
const seedDir = join(__dirname, '..', 'data', 'seed');
const outDir = join(__dirname, '..', 'supabase');
mkdirSync(outDir, { recursive: true });

const teams = JSON.parse(readFileSync(join(seedDir, 'teams.json'), 'utf8'));
const venues = JSON.parse(readFileSync(join(seedDir, 'venues.json'), 'utf8'));
const matches = JSON.parse(readFileSync(join(seedDir, 'schedule.json'), 'utf8'));
const hist = JSON.parse(readFileSync(join(seedDir, 'historical.json'), 'utf8'));

const q = (v) =>
  v === null || v === undefined
    ? 'NULL'
    : typeof v === 'number'
      ? String(v)
      : typeof v === 'boolean'
        ? v ? 'true' : 'false'
        : `'${String(v).replace(/'/g, "''")}'`;

function insertMany(table, cols, rows) {
  if (!rows.length) return '';
  const values = rows
    .map((r) => `  (${cols.map((c) => q(r[c])).join(', ')})`)
    .join(',\n');
  // Upsert: on PK conflict, update every non-PK column (idempotent re-seed).
  const updates = cols
    .slice(1)
    .map((c) => `${c} = EXCLUDED.${c}`)
    .join(', ');
  return `INSERT INTO ${table} (${cols.join(', ')}) VALUES\n${values}\nON CONFLICT (${cols[0]}) DO UPDATE SET ${updates};\n\n`;
}

let sql = `-- AUTO-GENERATED by scripts/gen-seed-sql.mjs — do not edit by hand.\n`;
sql += `-- Seeds teams, venues, matches, and historical data.\n\n`;

sql += insertMany(
  'venues',
  ['id', 'name', 'city', 'country', 'capacity', 'lat', 'lng', 'color'],
  venues,
);
sql += insertMany(
  'teams',
  ['id', 'name', 'name_es', 'flag_emoji', 'iso2', 'group_letter', 'host_country', 'api_football_id', 'confederation'],
  teams,
);
sql += insertMany(
  'matches',
  [
    'id', 'stage', 'group_letter', 'match_number', 'home_team_id', 'away_team_id',
    'home_placeholder', 'away_placeholder', 'home_score', 'away_score',
    'home_score_penalties', 'away_score_penalties', 'status', 'kickoff_utc',
    'venue_id', 'api_football_fixture_id', 'minute',
  ],
  matches,
);
sql += insertMany(
  'historical_editions',
  ['year', 'host', 'champion', 'runner_up', 'third_place', 'total_goals', 'total_teams', 'final_score', 'top_scorer'],
  hist.editions,
);

// historical_matches uses a SERIAL id — clear then insert (idempotent).
if (hist.matches.length) {
  const cols = ['year', 'stage', 'home_team', 'away_team', 'home_score', 'away_score', 'venue', 'match_date'];
  const values = hist.matches
    .map((r) => `  (${cols.map((c) => q(r[c])).join(', ')})`)
    .join(',\n');
  sql += `DELETE FROM historical_matches;\n`;
  sql += `INSERT INTO historical_matches (${cols.join(', ')}) VALUES\n${values};\n`;
}

writeFileSync(join(outDir, 'seed.sql'), sql);
console.log(
  `✅ seed.sql written — ${venues.length} venues, ${teams.length} teams, ${matches.length} matches, ${hist.editions.length} editions, ${hist.matches.length} historical matches`,
);
